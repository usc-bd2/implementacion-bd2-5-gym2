CREATE INDEX idx_reserva_usuario
  ON reserva (id_usuario);

CREATE INDEX idx_reserva_sesion
  ON reserva (id_sesion);

CREATE INDEX idx_plaza_reserva
  ON plaza (id_reserva);

CREATE INDEX idx_plaza_sala
  ON plaza (id_sala);

