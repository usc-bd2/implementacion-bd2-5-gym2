-- T1: Anti-solapamiento de sesiones en la misma sala
CREATE OR REPLACE FUNCTION fn_validar_solape_sesion()
RETURNS TRIGGER AS $$
DECLARE
  duracion_nueva INTEGER;
  inicio_nuevo   TIMESTAMP;
  fin_nuevo      TIMESTAMP;
BEGIN
  SELECT c.duracion INTO duracion_nueva
  FROM clase c
  WHERE c.nombre = NEW.nombre_clase;

  IF duracion_nueva IS NULL THEN
    RAISE EXCEPTION 'La clase % no existe o no tiene duración válida.', NEW.nombre_clase;
  END IF;

  inicio_nuevo := NEW.fecha_sesion::timestamp + NEW.hora_inicio;
  fin_nuevo    := inicio_nuevo + make_interval(mins => duracion_nueva);

  IF EXISTS (
    SELECT 1
    FROM sesion s
    JOIN clase c ON c.nombre = s.nombre_clase
    WHERE s.id_sala   = NEW.id_sala
      AND s.id_sesion <> COALESCE(NEW.id_sesion, -1)
      AND inicio_nuevo < (s.fecha_sesion::timestamp + s.hora_inicio
                          + make_interval(mins => c.duracion))
      AND (s.fecha_sesion::timestamp + s.hora_inicio) < fin_nuevo
  ) THEN
    RAISE EXCEPTION
      'No se puede insertar/actualizar la sesión: se solapa con otra en la misma sala.';
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_validar_solape_sesion
BEFORE INSERT OR UPDATE OF id_sala, nombre_clase, fecha_sesion, hora_inicio
ON sesion
FOR EACH ROW
EXECUTE FUNCTION fn_validar_solape_sesion();

-- T2: Herencia Disjunta — un trabajador no puede ser monitor Y limpieza
CREATE OR REPLACE FUNCTION fn_monitor_disjunto()
RETURNS TRIGGER AS $$
BEGIN
  IF EXISTS (SELECT 1 FROM limpieza WHERE dni = NEW.dni) THEN
    RAISE EXCEPTION
      'Un trabajador no puede pertenecer simultáneamente a monitor y limpieza.';
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_monitor_disjunto
BEFORE INSERT OR UPDATE OF dni ON monitor
FOR EACH ROW
EXECUTE FUNCTION fn_monitor_disjunto();

CREATE OR REPLACE FUNCTION fn_limpieza_disjunta()
RETURNS TRIGGER AS $$
BEGIN
  IF EXISTS (SELECT 1 FROM monitor WHERE dni = NEW.dni) THEN
    RAISE EXCEPTION
      'Un trabajador no puede pertenecer simultáneamente a limpieza y monitor.';
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_limpieza_disjunta
BEFORE INSERT OR UPDATE OF dni ON limpieza
FOR EACH ROW
EXECUTE FUNCTION fn_limpieza_disjunta();

-- T3: Herencia Total — todo trabajador debe tener subtipo
CREATE OR REPLACE FUNCTION fn_trabajador_debe_tener_subtipo()
RETURNS TRIGGER AS $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM monitor   WHERE dni = NEW.dni)
 AND NOT EXISTS (SELECT 1 FROM limpieza  WHERE dni = NEW.dni) THEN
    RAISE EXCEPTION
      'Todo trabajador debe pertenecer a una subclase: monitor o limpieza.';
  END IF;
  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE CONSTRAINT TRIGGER ctr_trabajador_debe_tener_subtipo
AFTER INSERT OR UPDATE OF dni ON trabajador
DEFERRABLE INITIALLY DEFERRED
FOR EACH ROW
EXECUTE FUNCTION fn_trabajador_debe_tener_subtipo();

-- T4: No dejar trabajador huérfano al borrar/mover subtipo
CREATE OR REPLACE FUNCTION fn_subtipo_no_puede_dejar_huerfano()
RETURNS TRIGGER AS $$
BEGIN
  IF EXISTS     (SELECT 1 FROM trabajador WHERE dni = OLD.dni)
 AND NOT EXISTS (SELECT 1 FROM monitor    WHERE dni = OLD.dni)
 AND NOT EXISTS (SELECT 1 FROM limpieza   WHERE dni = OLD.dni) THEN
    RAISE EXCEPTION 'No se puede dejar un trabajador sin subtipo.';
  END IF;
  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE CONSTRAINT TRIGGER ctr_monitor_no_huerfano
AFTER DELETE OR UPDATE OF dni ON monitor
DEFERRABLE INITIALLY DEFERRED
FOR EACH ROW
EXECUTE FUNCTION fn_subtipo_no_puede_dejar_huerfano();

CREATE CONSTRAINT TRIGGER ctr_limpieza_no_huerfano
AFTER DELETE OR UPDATE OF dni ON limpieza
DEFERRABLE INITIALLY DEFERRED
FOR EACH ROW
EXECUTE FUNCTION fn_subtipo_no_puede_dejar_huerfano();

